// node 04_Controlflow/switch.js


const month = 3

switch (month) {
    case 1:
        
        console.log("January");
        break;  // --> breaks control flow
    case 2:
        
        console.log("February");
        break;
    case 3:
        
        console.log("March");
        break;
    case 4:
        
        console.log("April");
        break;

    default:
        break;
}